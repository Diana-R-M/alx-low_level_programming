#include "lists.h"
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <stddef.h>

/**
 * print_list - prints elements of the linked list
 * @h: pointer to the linked list
 * Return: nodes to be printed
 */

size_t print_list(const list_t *h)
{
	size_t n;

	n = 0;

	while (h)
	{
		if (!h->str)
			printf("[0] (nil)\n");
		else
			printf("[%u] %s\n", h->len, h->str);
		h = h->next;
		n++;
	}
	return (n);
}
